#!/usr/bin/env bash
# install-chitin-bench-cron.sh — idempotent installer for chitin-bench-watcher cron
#
# Per Constitution §6: tracked source + idempotent installer.
# Per ic-001 spec §Invariants: hermes cron required (loud-fail if missing).
# Per Clawta sw-009-round-2 amendment: NO system-crontab fallback (silent
# no-op was the bug; loud-fail-early is the bright line).
#
# Usage:
#   ./install-chitin-bench-cron.sh           # install/update
#   ./install-chitin-bench-cron.sh --verify  # check if installed
#   ./install-chitin-bench-cron.sh --remove  # uninstall

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
WATCHER="$SCRIPT_DIR/chitin-bench-watcher"
JOB_NAME="chitin-bench-watcher"
MARKER="# chitin-bench-watcher-cron-managed"

JOBS_FILE="${HOME}/.hermes/cron/jobs.json"
WRAPPER_FILENAME="chitin-bench-watcher-wrapper.sh"
WRAPPER="${HOME}/.hermes/scripts/${WRAPPER_FILENAME}"

BOARD="${KANBAN_BOARD:-chitin-bench}"
MODEL="${CHITIN_BENCH_MODEL:-qwen3-coder:30b-32k}"

# ── Verify watcher exists ──────────────────────────────────────────

if [[ ! -x "$WATCHER" ]]; then
    echo "FATAL: chitin-bench-watcher not found or not executable at $WATCHER" >&2
    exit 1
fi

# ── Check for existing job ─────────────────────────────────────────

check_existing() {
    if [[ -f "$JOBS_FILE" ]]; then
        if python3 -c "
import json, sys
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$JOB_NAME' and j.get('enabled', True) and j.get('state') != 'paused':
        sys.exit(0)
sys.exit(1)
" 2>/dev/null; then
            echo "hermes-cron"
            return
        fi
    fi
    if crontab -l 2>/dev/null | grep -q "$MARKER"; then
        echo "system-crontab"
        return
    fi
    echo "none"
}

# ── Generate wrapper script ────────────────────────────────────────

generate_wrapper() {
    mkdir -p "$(dirname "$WRAPPER")"
    cat > "$WRAPPER" <<WRAPPER_EOF
#!/usr/bin/env bash
$MARKER
# chitin-bench-watcher-wrapper — cron entry for the chitin-bench local-LLM driver.
# Generated by install-chitin-bench-cron.sh. Do not edit directly.
set -eo pipefail
set -a
. "\${HOME}/.hermes/.env" 2>/dev/null || true
set +a
export KANBAN_BOARD="${BOARD}"
export CHITIN_BENCH_MODEL="${MODEL}"
exec python3 "${WATCHER}" --board "${BOARD}" --model "${MODEL}"
WRAPPER_EOF
    chmod +x "$WRAPPER"
    echo "[install] wrapper installed: $WRAPPER"
}

# ── Actions ────────────────────────────────────────────────────────

case "${1:-install}" in
    --verify)
        backend=$(check_existing)
        if [[ "$backend" == "none" ]]; then
            echo "NOT INSTALLED"
            exit 1
        else
            echo "INSTALLED via $backend"
            exit 0
        fi
        ;;

    --remove)
        backend=$(check_existing)
        if [[ "$backend" == "hermes-cron" ]]; then
            if [[ -f "$JOBS_FILE" ]]; then
                job_ids=$(python3 -c "
import json
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$JOB_NAME':
        print(j.get('id', ''))
" 2>/dev/null)
                for job_id in $job_ids; do
                    hermes cron remove "$job_id" 2>/dev/null || true
                done
                echo "[remove] removed hermes cron job(s) $JOB_NAME"
            fi
        elif [[ "$backend" == "system-crontab" ]]; then
            remaining=$(crontab -l 2>/dev/null | grep -v "$MARKER" || true)
            if [[ -z "$remaining" ]]; then
                crontab -r 2>/dev/null || true
            else
                echo "$remaining" | crontab -
            fi
            echo "[remove] removed system crontab entry"
        else
            echo "[remove] nothing to remove"
        fi
        if [[ -f "$WRAPPER" ]]; then
            rm -f "$WRAPPER"
            echo "[remove] cleaned up wrapper: $WRAPPER"
        fi
        exit 0
        ;;

    install|"")
        backend=$(check_existing)
        if [[ "$backend" != "none" ]]; then
            echo "[install] chitin-bench-watcher cron already installed via $backend (idempotent - skipping)"
            exit 0
        fi

        # Per Clawta sw-009 round-2: hermes is REQUIRED. There is no
        # system-crontab fallback because system crontab only logs the
        # script output — it does NOT inject stdout into the chitin-bench
        # agent context (chitin-bench IS the watcher, not an agent — so the
        # plain script suffices, but we still match the swarm-invoker
        # loud-fail pattern for installer consistency).
        # Loud failure > silent recovery (swarm redesign bright line 1).
        if ! command -v hermes &>/dev/null; then
            cat >&2 <<HERMES_MISSING
FATAL: hermes CLI not on PATH.

The chitin-bench-watcher uses hermes cron for the same scheduler primitive
as swarm-invoker (sw-009). A system-crontab fallback was considered
but rejected per Clawta sw-009 round-2 review (loud failure > silent
recovery).

Fix: install hermes-agent (see chitin docs) then re-run:
  $0
HERMES_MISSING
            exit 2
        fi

        generate_wrapper

        echo "[install] registering hermes cron job: $JOB_NAME"
        if hermes cron create \
            --name "$JOB_NAME" \
            --script "$WRAPPER_FILENAME" \
            --no-agent \
            --deliver discord:#chitin-bench \
            "every 1m" \
            "chitin-bench-watcher: poll chitin-bench board for lint-fix tickets and dispatch local LLM" \
            ; then
            echo "[install] hermes cron job registered: $JOB_NAME (every 1m, board=$BOARD, model=$MODEL)"
            echo "[install] mode: --no-agent (chitin-bench IS the driver; stdout = WORKER_RECEIPT)"
            echo "[install] verify with: $0 --verify"
            exit 0
        else
            cat >&2 <<HERMES_CREATE_FAILED
FATAL: hermes cron create failed for $JOB_NAME.

Install aborted. The wrapper was generated at $WRAPPER but no cron
entry was added. Inspect the hermes-cron error above, fix the
underlying issue, then re-run this installer.
HERMES_CREATE_FAILED
            rm -f "$WRAPPER" 2>/dev/null || true
            exit 3
        fi
        ;;

    *)
        echo "Usage: $0 [--verify|--remove|install]" >&2
        exit 1
        ;;
esac
