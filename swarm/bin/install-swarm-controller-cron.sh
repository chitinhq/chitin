#!/usr/bin/env bash
# install-swarm-controller-cron.sh — idempotent installer for the swarm-controller loop
#
# Per Constitution S6: tracked source + idempotent installer.
# Generates a wrapper script at ~/.hermes/scripts/ and registers
# a hermes cron job that runs it every 60s. Falls back to system
# crontab if hermes cron is unavailable.
#
# Usage:
#   ./install-swarm-controller-cron.sh          # install/update
#   ./install-swarm-controller-cron.sh --verify  # check if installed
#   ./install-swarm-controller-cron.sh --remove  # uninstall
#
# Env:
#   KANBAN_BOARD         board name (default: swarm)
#   TICK_SECONDS         tick interval (default: 60)
#   CHITIN_REPO          path to chitin checkout (derived from script location if unset)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
CONTROLLER="$SCRIPT_DIR/swarm-controller"
JOB_NAME="swarm-controller-loop"

BOARD="${KANBAN_BOARD:-swarm}"
TICK="${TICK_SECONDS:-60}"
# CHITIN_REPO must be set in env; fallback derives from script location
# (SCRIPT_DIR is swarm/bin, repo root is two levels up)
REPO="${CHITIN_REPO:-$(cd "$SCRIPT_DIR/../.." && pwd)}"

WRAPPER_FILENAME="swarm-controller-loop-wrapper.sh"
WRAPPER="${HOME}/.hermes/scripts/${WRAPPER_FILENAME}"

# ── Verify controller exists ──────────────────────────────────────

if [[ ! -x "$CONTROLLER" ]]; then
    echo "FATAL: swarm-controller not found or not executable at $CONTROLLER" >&2
    exit 1
fi

# ── Check for existing job ─────────────────────────────────────────

check_existing() {
    if command -v hermes &>/dev/null; then
        if hermes cron list 2>/dev/null | grep -q "$JOB_NAME"; then
            echo "hermes-cron"
            return
        fi
    fi
    if crontab -l 2>/dev/null | grep -q "swarm-controller.*--loop"; then
        echo "system-crontab"
        return
    fi
    echo "none"
}

# ── Generate wrapper script ────────────────────────────────────────
# Blocker 2 fix: the cron must run --loop, not --once.
# Generate a wrapper that execs the controller with the correct args
# so hermes cron just calls the wrapper (no args needed).

generate_wrapper() {
    mkdir -p "$(dirname "$WRAPPER")"
    cat > "$WRAPPER" <<WRAPPER_EOF
#!/usr/bin/env bash
# swarm-controller-loop-wrapper — cron entry point for the dispatch loop.
# Generated by install-swarm-controller-cron.sh. Do not edit directly.
set -eo pipefail
set -a
. "\${HOME}/.hermes/.env" 2>/dev/null || true
set +a
export KANBAN_BOARD="${BOARD}"
export CHITIN_REPO="${REPO}"
# Cron wrapper: run single tick. Receipts are posted by the controller
# internally via openclaw (to #swarm). Cron no-agent mode delivers
# stdout verbatim — empty stdout = no message sent. We redirect
# everything to /dev/null so the cron only fires if the script itself
# fails (non-zero exit → error alert from the cron scheduler).
exec python3 "${CONTROLLER}" --once --board "${BOARD}" >/dev/null 2>&1
WRAPPER_EOF
    chmod +x "$WRAPPER"
    echo "[install] wrapper installed: $WRAPPER"
}

# ── Actions ────────────────────────────────────────────────────────

case "${1:-install}" in
    --verify)
        backend=$(check_existing)
        if [[ "$backend" == "none" ]]; then
            echo "NOT INSTALLED"
            exit 1
        else
            echo "INSTALLED via $backend"
            exit 0
        fi
        ;;

    --remove)
        backend=$(check_existing)
        if [[ "$backend" == "hermes-cron" ]]; then
            if command -v hermes &>/dev/null; then
                job_ids=$(hermes cron list 2>/dev/null | grep "$JOB_NAME" | awk '{print $1}')
                for job_id in $job_ids; do
                    hermes cron remove "$job_id" 2>/dev/null || true
                done
                echo "[remove] removed hermes cron job(s) named $JOB_NAME"
            fi
        elif [[ "$backend" == "system-crontab" ]]; then
            crontab -l 2>/dev/null | grep -v "swarm-controller.*--loop" | crontab -
            echo "[remove] removed system crontab entry"
        else
            echo "[remove] nothing to remove"
        fi
        # Always clean up the wrapper
        if [[ -f "$WRAPPER" ]]; then
            rm -f "$WRAPPER"
            echo "[remove] cleaned up wrapper: $WRAPPER"
        fi
        exit 0
        ;;

    install|"")
        backend=$(check_existing)
        if [[ "$backend" != "none" ]]; then
            echo "[install] swarm-controller cron already installed via $backend (idempotent - skipping)"
            exit 0
        fi

        # Blocker 2: generate the wrapper first
        generate_wrapper

        # Blocker 1 fix: hermes cron create takes schedule as a POSITIONAL arg,
        # not --schedule flag. Correct shape:
        #   hermes cron create --name N --script S --no-agent "every 60s"
        if command -v hermes &>/dev/null; then
            echo "[install] registering hermes cron job: $JOB_NAME"
            # Blocker 1 fix: --script takes RELATIVE filename under ~/.hermes/scripts/,
            # not absolute path. Schedule is positional (not --schedule flag),
            # and uses hermes duration format: "every 1m" not "1m" (which is one-shot).
            if hermes cron create \
                --name "$JOB_NAME" \
                --script "$WRAPPER_FILENAME" \
                --no-agent \
                --deliver "discord:#swarm" \
                "every 1m" \
                ; then
                echo "[install] hermes cron job registered: $JOB_NAME (every 60s, board=$BOARD)"
                echo "[install] heartbeat receipts to #swarm every 5 ticks (5 min)"
                echo "[install] verify with: hermes cron list | grep $JOB_NAME"
                exit 0
            else
                echo "[install] hermes cron create failed, falling back to system crontab"
            fi
        fi

        # Fallback: system crontab (also uses --once, cron handles repeat)
        cron_entry="* * * * * KANBAN_BOARD=$BOARD $CONTROLLER --once --board $BOARD >> $HOME/.openclaw/logs/swarm-controller-cron.log 2>&1"
        (crontab -l 2>/dev/null; echo "$cron_entry") | crontab -
        echo "[install] system crontab entry added (every 1 min, board=$BOARD)"
        exit 0
        ;;

    *)
        echo "Usage: $0 [--verify|--remove|install]" >&2
        exit 1
        ;;
esac