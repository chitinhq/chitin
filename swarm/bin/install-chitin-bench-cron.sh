#!/usr/bin/env bash
# install-chitin-bench-cron.sh — idempotent installer for the
# Chitin Bench terminal-bench loop.
#
# Registers two hermes cron jobs:
#   1. chitin-bench-runner   (every 2h) — picks N cached harbor tasks
#                                          and runs them via the Chitin Bench
#                                          harness; emits tickets on
#                                          failure to the chitin-bench board.
#   2. chitin-bench-emitter  (every 30m) — sweeper for any failed
#                                          trials whose tickets weren't
#                                          emitted (defense in depth;
#                                          the runner also calls the
#                                          emitter directly post-tick).
#
# Constitution §6: tracked source + idempotent installer.
# Per sw-009 round-2: NO system-crontab fallback. hermes required.
#
# Usage:
#   ./install-chitin-bench-cron.sh           # install/update
#   ./install-chitin-bench-cron.sh --verify  # check
#   ./install-chitin-bench-cron.sh --remove  # uninstall

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
RUNNER="$SCRIPT_DIR/chitin-bench-runner"
EMITTER="$SCRIPT_DIR/chitin-bench-ticket-emitter"

JOBS_FILE="${HOME}/.hermes/cron/jobs.json"
WRAPPER_DIR="${HOME}/.hermes/scripts"
RUNNER_WRAPPER_NAME="chitin-bench-runner-wrapper.sh"
EMITTER_WRAPPER_NAME="chitin-bench-emitter-wrapper.sh"
RUNNER_WRAPPER="$WRAPPER_DIR/$RUNNER_WRAPPER_NAME"
EMITTER_WRAPPER="$WRAPPER_DIR/$EMITTER_WRAPPER_NAME"

MARKER="# chitin-bench-cron-managed"

MODEL="${CHITIN_BENCH_MODEL:-ollama/qwen3-coder:30b-32k}"
N_TASKS="${CHITIN_BENCH_N_TASKS:-1}"

JOB_NAME_RUNNER="chitin-bench-runner"
JOB_NAME_EMITTER="chitin-bench-emitter"

# ── Sanity ─────────────────────────────────────────────────────────

if [[ ! -x "$RUNNER" ]]; then
    echo "FATAL: $RUNNER not found or not executable" >&2
    exit 1
fi
if [[ ! -x "$EMITTER" ]]; then
    echo "FATAL: $EMITTER not found or not executable" >&2
    exit 1
fi

# ── Helpers ────────────────────────────────────────────────────────

check_job_registered() {
    local name="$1"
    if [[ -f "$JOBS_FILE" ]] && python3 -c "
import json, sys
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$name' and j.get('enabled', True) and j.get('state') != 'paused':
        sys.exit(0)
sys.exit(1)
" 2>/dev/null; then
        return 0
    fi
    return 1
}

generate_runner_wrapper() {
    mkdir -p "$WRAPPER_DIR"
    cat > "$RUNNER_WRAPPER" <<WRAPPER_EOF
#!/usr/bin/env bash
$MARKER
# Generated by install-chitin-bench-cron.sh — do not edit.
set -eo pipefail
set -a
. "\${HOME}/.hermes/.env" 2>/dev/null || true
set +a
export PYTHONPATH="${REPO_ROOT}\${PYTHONPATH:+:\$PYTHONPATH}"
export CHITIN_BENCH_MODEL="${MODEL}"
export CHITIN_BENCH_N_TASKS="${N_TASKS}"
exec "${RUNNER}"
WRAPPER_EOF
    chmod +x "$RUNNER_WRAPPER"
    echo "[install] wrote $RUNNER_WRAPPER"
}

generate_emitter_wrapper() {
    mkdir -p "$WRAPPER_DIR"
    cat > "$EMITTER_WRAPPER" <<WRAPPER_EOF
#!/usr/bin/env bash
$MARKER
# Generated by install-chitin-bench-cron.sh — do not edit.
set -eo pipefail
set -a
. "\${HOME}/.hermes/.env" 2>/dev/null || true
set +a
exec "${EMITTER}"
WRAPPER_EOF
    chmod +x "$EMITTER_WRAPPER"
    echo "[install] wrote $EMITTER_WRAPPER"
}

require_hermes() {
    if ! command -v hermes &>/dev/null; then
        cat >&2 <<HERMES_MISSING
FATAL: hermes CLI not on PATH.

The chitin-bench cron uses hermes-cron (no system-crontab fallback per
sw-009 round-2 — loud failure > silent recovery). Install hermes-agent
and re-run.
HERMES_MISSING
        exit 2
    fi
}

# ── Actions ────────────────────────────────────────────────────────

case "${1:-install}" in
    --verify)
        ok=true
        if check_job_registered "$JOB_NAME_RUNNER"; then
            echo "INSTALLED runner: $JOB_NAME_RUNNER"
        else
            echo "MISSING runner: $JOB_NAME_RUNNER"
            ok=false
        fi
        if check_job_registered "$JOB_NAME_EMITTER"; then
            echo "INSTALLED emitter: $JOB_NAME_EMITTER"
        else
            echo "MISSING emitter: $JOB_NAME_EMITTER"
            ok=false
        fi
        $ok && exit 0 || exit 1
        ;;

    --remove)
        require_hermes
        for name in "$JOB_NAME_RUNNER" "$JOB_NAME_EMITTER"; do
            if [[ -f "$JOBS_FILE" ]]; then
                ids=$(python3 -c "
import json
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$name':
        print(j.get('id', ''))
" 2>/dev/null)
                for jid in $ids; do
                    [[ -n "$jid" ]] && hermes cron remove "$jid" 2>/dev/null || true
                done
                echo "[remove] removed $name"
            fi
        done
        rm -f "$RUNNER_WRAPPER" "$EMITTER_WRAPPER"
        exit 0
        ;;

    install|"")
        require_hermes
        generate_runner_wrapper
        generate_emitter_wrapper

        # Runner: every 2 hours.
        if check_job_registered "$JOB_NAME_RUNNER"; then
            echo "[install] $JOB_NAME_RUNNER already registered (idempotent)"
        else
            echo "[install] registering $JOB_NAME_RUNNER"
            if ! hermes cron create \
                --name "$JOB_NAME_RUNNER" \
                --script "$RUNNER_WRAPPER_NAME" \
                --no-agent \
                --deliver discord:#chitin-bench \
                "every 120m" \
                "chitin-bench-runner: pick N LRU cached terminal-bench tasks and run the Chitin Bench harness against each; on failures, post tickets to the chitin-bench board via the emitter"; then
                echo "FATAL: hermes cron create failed for $JOB_NAME_RUNNER" >&2
                exit 3
            fi
        fi

        # Emitter sweeper: every 30 minutes (catches any failed trials
        # the runner couldn't emit for in-line, plus picks up trials
        # written outside the runner path).
        if check_job_registered "$JOB_NAME_EMITTER"; then
            echo "[install] $JOB_NAME_EMITTER already registered (idempotent)"
        else
            echo "[install] registering $JOB_NAME_EMITTER"
            if ! hermes cron create \
                --name "$JOB_NAME_EMITTER" \
                --script "$EMITTER_WRAPPER_NAME" \
                --no-agent \
                --deliver discord:#chitin-bench \
                "every 30m" \
                "chitin-bench-emitter: sweep jobs/chitin-bench/ for failed trials and post tickets to the chitin-bench board (deduped via idempotency key)"; then
                echo "WARN: hermes cron create failed for $JOB_NAME_EMITTER (runner still emits in-line, sweeper is defence in depth)" >&2
            fi
        fi

        echo "[install] verify with: $0 --verify"
        echo ""
        echo "Smoke test (runs one task):"
        echo "  PYTHONPATH=${REPO_ROOT} CHITIN_BENCH_N_TASKS=1 ${RUNNER} --shuffle"
        exit 0
        ;;

    *)
        echo "Usage: $0 [install|--verify|--remove]" >&2
        exit 1
        ;;
esac
