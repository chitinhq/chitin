#!/usr/bin/env bash
# install-swarm-invoker-cron.sh — idempotent installer for the swarm-invoker
#
# Per Constitution S6: tracked source + idempotent installer.
# Registers a hermes cron job that runs the invoker every 1m.
# The invoker is a --script (NOT --no-agent): its stdout gets injected
# into the agent prompt, so the agent reasons over actionable tickets.
# Empty stdout = no tickets = no invocation = silent delivery.
#
# Usage:
#   ./install-swarm-invoker-cron.sh          # install/update
#   ./install-swarm-invoker-cron.sh --verify  # check if installed
#   ./install-swarm-invoker-cron.sh --remove  # uninstall

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
INVOKER="$SCRIPT_DIR/swarm-invoker"
JOB_NAME="swarm-invoker"
MARKER="# swarm-invoker-cron-managed"

JOBS_FILE="${HOME}/.hermes/cron/jobs.json"
WRAPPER_FILENAME="swarm-invoker-wrapper.sh"
WRAPPER="${HOME}/.hermes/scripts/${WRAPPER_FILENAME}"

BOARD="${KANBAN_BOARD:-swarm}"

# ── Verify invoker exists ──────────────────────────────────────────

if [[ ! -x "$INVOKER" ]]; then
    echo "FATAL: swarm-invoker not found or not executable at $INVOKER" >&2
    exit 1
fi

# ── Check for existing job ─────────────────────────────────────────

check_existing() {
    if [[ -f "$JOBS_FILE" ]]; then
        if python3 -c "
import json, sys
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$JOB_NAME' and j.get('enabled', True) and j.get('state') != 'paused':
        sys.exit(0)
sys.exit(1)
" 2>/dev/null; then
            echo "hermes-cron"
            return
        fi
    fi
    if crontab -l 2>/dev/null | grep -q "$MARKER"; then
        echo "system-crontab"
        return
    fi
    echo "none"
}

# ── Generate wrapper script ────────────────────────────────────────

generate_wrapper() {
    mkdir -p "$(dirname "$WRAPPER")"
    cat > "$WRAPPER" <<WRAPPER_EOF
#!/usr/bin/env bash
$MARKER
# swarm-invoker-wrapper — cron entry point for agent invocation.
# Generated by install-swarm-invoker-cron.sh. Do not edit directly.
set -eo pipefail
set -a
. "\${HOME}/.hermes/.env" 2>/dev/null || true
set +a
export KANBAN_BOARD="${BOARD}"
exec python3 "${INVOKER}" --board "${BOARD}"
WRAPPER_EOF
    chmod +x "$WRAPPER"
    echo "[install] wrapper installed: $WRAPPER"
}

# ── Actions ────────────────────────────────────────────────────────

case "${1:-install}" in
    --verify)
        backend=$(check_existing)
        if [[ "$backend" == "none" ]]; then
            echo "NOT INSTALLED"
            exit 1
        else
            echo "INSTALLED via $backend"
            exit 0
        fi
        ;;

    --remove)
        backend=$(check_existing)
        if [[ "$backend" == "hermes-cron" ]]; then
            if [[ -f "$JOBS_FILE" ]]; then
                job_ids=$(python3 -c "
import json
jobs = json.load(open('$JOBS_FILE')).get('jobs', [])
for j in jobs:
    if j.get('name') == '$JOB_NAME':
        print(j.get('id', ''))
" 2>/dev/null)
                for job_id in $job_ids; do
                    hermes cron remove "$job_id" 2>/dev/null || true
                done
                echo "[remove] removed hermes cron job(s) $JOB_NAME"
            fi
        elif [[ "$backend" == "system-crontab" ]]; then
            remaining=$(crontab -l 2>/dev/null | grep -v "$MARKER" || true)
            if [[ -z "$remaining" ]]; then
                crontab -r 2>/dev/null || true
            else
                echo "$remaining" | crontab -
            fi
            echo "[remove] removed system crontab entry"
        else
            echo "[remove] nothing to remove"
        fi
        if [[ -f "$WRAPPER" ]]; then
            rm -f "$WRAPPER"
            echo "[remove] cleaned up wrapper: $WRAPPER"
        fi
        exit 0
        ;;

    install|"")
        backend=$(check_existing)
        if [[ "$backend" != "none" ]]; then
            echo "[install] swarm-invoker cron already installed via $backend (idempotent - skipping)"
            exit 0
        fi

        generate_wrapper

        # Key: this is --script WITHOUT --no-agent.
        # The invoker's stdout (ticket prompts) gets injected into the
        # agent's context. Empty stdout = no invocation = silent.
        if command -v hermes &>/dev/null; then
            echo "[install] registering hermes cron job: $JOB_NAME"
            if hermes cron create \
                --name "$JOB_NAME" \
                --script "$WRAPPER_FILENAME" \
                --deliver discord:#swarm \
                "every 1m" \
                "swarm-invoker: check swarm board for actionable tickets assigned to hermes and action them" \
                ; then
                echo "[install] hermes cron job registered: $JOB_NAME (every 1m, board=$BOARD)"
                echo "[install] mode: agent-invocation (script stdout → agent prompt)"
                echo "[install] verify with: $0 --verify"
                exit 0
            else
                echo "[install] hermes cron create failed, falling back to system crontab"
            fi
        fi

        # Fallback: system crontab
        cron_entry="* * * * * KANBAN_BOARD=$BOARD $INVOKER --board $BOARD >> $HOME/.openclaw/logs/swarm-invoker-cron.log 2>&1 $MARKER"
        (crontab -l 2>/dev/null; echo "$cron_entry") | crontab -
        echo "[install] system crontab entry added (every 1 min, board=$BOARD)"
        exit 0
        ;;

    *)
        echo "Usage: $0 [--verify|--remove|install]" >&2
        exit 1
        ;;
esac